public class TheaterLights {

    public void on()
    {
        System.out.println("Theater Ceiling lights on ");
    }
    public void off()
    {
        System.out.println("Theater Ceiling Lights off");
    }
    public void dim(int dim)
    {
        System.out.println("Theater Ceiling Lights dimming to "+dim+"%");
    }
}
